<?php
echo "<h3>Switch Case</h3>";
$x = 3;
echo "$x <br>";

switch($x)
{
case 1:
	echo "Monday";
	break;
case 2:
	echo "Tuesday";
	break;
case 3:
	echo "Wednesday";
	break;
case 4:
	echo "Thursday";
	break;
case 5:
	echo "Friday";
	break;
case 6:
	echo "Sunday";
	break;
case 7:
	echo "Sunday";
	break;
default:
	echo "Invalid....";
}
?>
